require_once __DIR__ . '/logger.php'; // –§—É–Ω–∫—Ü–∏–∏ –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è –¥–æ—Å—Ç—É–ø–Ω—ã —Å—Ä–∞–∑—É
require_once 'calc.php'; // –ü–æ–¥–∫–ª—é—á–∞–µ–º —Ñ—É–Ω–∫—Ü–∏–∏ —Ä–∞—Å—á—ë—Ç–∞ –±—Ä–∞—Å–ª–µ—Ç–∞
// –§—É–Ω–∫—Ü–∏–∏ –ø—Ä–æ–≤–µ—Ä–∫–∏ IP-–∞–¥—Ä–µ—Å–æ–≤ Telegram –≤—ã–Ω–µ—Å–µ–Ω—ã –≤ –æ—Ç–¥–µ–ª—å–Ω—ã–π —Ñ–∞–π–ª,
// —á—Ç–æ–±—ã –∏—Ö –º–æ–∂–Ω–æ –±—ã–ª–æ –ø–µ—Ä–µ–∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –∏ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å –∏–∑–æ–ª–∏—Ä–æ–≤–∞–Ω–Ω–æ.
require_once __DIR__ . '/telegram_ip.php'; // –ü–æ–¥–∫–ª—é—á–∞–µ–º —Ñ—É–Ω–∫—Ü–∏–∏ –ø—Ä–æ–≤–µ—Ä–∫–∏ IP

/**
 * –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç IP-–∞–¥—Ä–µ—Å –∫–ª–∏–µ–Ω—Ç–∞, —É—á–∏—Ç—ã–≤–∞—è –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å —Ä–∞–±–æ—Ç—ã –∑–∞ –ø—Ä–æ–∫—Å–∏.
 *
 * –í–ê–ñ–ù–û: —Ñ—É–Ω–∫—Ü–∏—è –ø—Ä–µ–¥–ø–æ–ª–∞–≥–∞–µ—Ç, —á—Ç–æ –∫–ª—é—á–∏ –º–∞—Å—Å–∏–≤–∞ –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤ —É–∂–µ –ø—Ä–∏–≤–µ–¥–µ–Ω—ã
 * –≤—ã–∑—ã–≤–∞—é—â–∏–º –∫–æ–¥–æ–º –∫ –Ω–∏–∂–Ω–µ–º—É —Ä–µ–≥–∏—Å—Ç—Ä—É. –≠—Ç–æ –∏—Å–∫–ª—é—á–∞–µ—Ç –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç—å –æ—Ç –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–µ–π
 * –≤–µ–±‚Äë—Å–µ—Ä–≤–µ—Ä–∞ –∏ –ø–æ–∑–≤–æ–ª—è–µ—Ç –æ–±—Ä–∞—â–∞—Ç—å—Å—è –∫ –∑–∞–≥–æ–ª–æ–≤–∫–∞–º –±–µ–∑ —É—á—ë—Ç–∞ —Ä–µ–≥–∏—Å—Ç—Ä–∞. –ü—Ä–∏
 * –Ω–∞–ª–∏—á–∏–∏ –¥–æ–≤–µ—Ä–µ–Ω–Ω–æ–≥–æ –ø—Ä–æ–∫—Å–∏ –º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –∑–∞–≥–æ–ª–æ–≤–æ–∫ `X-Forwarded-For`,
 * –∫–æ—Ç–æ—Ä—ã–π —Å–æ–¥–µ—Ä–∂–∏—Ç —Ü–µ–ø–æ—á–∫—É IP‚Äë–∞–¥—Ä–µ—Å–æ–≤. –ü–µ—Ä–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç —ç—Ç–æ–≥–æ —Å–ø–∏—Å–∫–∞ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç
 * –∏—Å—Ö–æ–¥–Ω–æ–º—É –æ—Ç–ø—Ä–∞–≤–∏—Ç–µ–ª—é –∑–∞–ø—Ä–æ—Å–∞. –ï—Å–ª–∏ –ø–æ–ª—É—á–µ–Ω–Ω—ã–π IP –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–µ–Ω –∏–ª–∏ –ø—Ä–æ–∫—Å–∏ –Ω–µ
 * –¥–æ–≤–µ—Ä–µ–Ω, —Å–ª–µ–¥—É–µ—Ç –æ–ø–∏—Ä–∞—Ç—å—Å—è —Ç–æ–ª—å–∫–æ –Ω–∞ `REMOTE_ADDR`.
 *
 * @param array<string,string> $headers        –ó–∞–≥–æ–ª–æ–≤–∫–∏ –≤—Ö–æ–¥—è—â–µ–≥–æ HTTP-–∑–∞–ø—Ä–æ—Å–∞
 *        —Å –∏–º–µ–Ω–∞–º–∏, –ø—Ä–∏–≤–µ–¥—ë–Ω–Ω—ã–º–∏ –∫ –Ω–∏–∂–Ω–µ–º—É —Ä–µ–≥–∏—Å—Ç—Ä—É.
 * @param array<string,mixed>  $server         –î–∞–Ω–Ω—ã–µ –æ –∑–∞–ø—Ä–æ—Å–µ, –æ–±—ã—á–Ω–æ `$_SERVER`.
 * @param bool                 $trustForwarded –ü—Ä–∏–∑–Ω–∞–∫ –¥–æ–≤–µ—Ä–∏—è –∫ –∑–∞–≥–æ–ª–æ–≤–∫—É
 *        `X-Forwarded-For`. –ü—Ä–∏ `false` –∑–Ω–∞—á–µ–Ω–∏–µ –∑–∞–≥–æ–ª–æ–≤–∫–∞ –∏–≥–Ω–æ—Ä–∏—Ä—É–µ—Ç—Å—è.
 *
 * @return string IP-–∞–¥—Ä–µ—Å –∫–ª–∏–µ–Ω—Ç–∞. –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç—Å—è, –µ—Å–ª–∏ –Ω–∏ –æ–¥–∏–Ω
 *                –∏–∑ –∏—Å—Ç–æ—á–Ω–∏–∫–æ–≤ –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç –≤–∞–ª–∏–¥–Ω–æ–≥–æ –∞–¥—Ä–µ—Å–∞.
*/
function resolveRemoteIp(array $headers, array $server, bool $trustForwarded = false): string
{
    // –ò—Å–ø–æ–ª—å–∑—É–µ–º IP –∏–∑ X-Forwarded-For —Ç–æ–ª—å–∫–æ –ø—Ä–∏ –¥–æ–≤–µ—Ä–µ–Ω–Ω–æ–º –ø—Ä–æ–∫—Å–∏
    if ($trustForwarded && !empty($headers['x-forwarded-for'])) {
        $forwarded = explode(',', $headers['x-forwarded-for']);
        $candidate = trim($forwarded[0]); // –ü–µ—Ä–≤—ã–π IP –∏–∑ —Ü–µ–ø–æ—á–∫–∏

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç—å IP-–∞–¥—Ä–µ—Å–∞ –∏–∑ –∑–∞–≥–æ–ª–æ–≤–∫–∞
        if (filter_var($candidate, FILTER_VALIDATE_IP)) {
            return $candidate; // –í–æ–∑–≤—Ä–∞—â–∞–µ–º –≤–∞–ª–∏–¥–Ω—ã–π –∞–¥—Ä–µ—Å
        }
        // –ï—Å–ª–∏ IP –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–µ–Ω, –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –∏ –∏—Å–ø–æ–ª—å–∑—É–µ–º REMOTE_ADDR
    }

    // –í–æ–∑–≤—Ä–∞—â–∞–µ–º REMOTE_ADDR, –µ—Å–ª–∏ –∑–∞–≥–æ–ª–æ–≤–æ–∫ –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç, –ø—Ä–æ–∫—Å–∏ –Ω–µ –¥–æ–≤–µ—Ä–µ–Ω –∏–ª–∏ IP –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–µ–Ω
    return $server['REMOTE_ADDR'] ?? '';
}

if (!defined('WEBHOOK_LIB')):

try {
    // –ü–æ–¥–∫–ª—é—á–∞–µ–º –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é, –≥–¥–µ –∑–∞–¥–∞—é—Ç—Å—è –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è
    require 'config.php';
} catch (Throwable $e) {
    // –ï—Å–ª–∏ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é –∑–∞–≥—Ä—É–∑–∏—Ç—å –Ω–µ —É–¥–∞–ª–æ—Å—å,
    // —Ñ–∏–∫—Å–∏—Ä—É–µ–º –æ—à–∏–±–∫—É –∏ –ø—Ä–æ–±—Ä–∞—Å—ã–≤–∞–µ–º –∏—Å–∫–ª—é—á–µ–Ω–∏–µ –¥–∞–ª—å—à–µ,
    // —á—Ç–æ–±—ã —Ç–µ—Å—Ç—ã –∏–ª–∏ –¥—Ä—É–≥–∏–µ —Å–∫—Ä–∏–ø—Ç—ã –º–æ–≥–ª–∏ –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å –µ—ë —Å–∞–º–æ—Å—Ç–æ—è—Ç–µ–ª—å–Ω–æ
    logError('–û—à–∏–±–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏: ' . $e->getMessage());
    throw $e;
}

// –ü–æ–ª—É—á–∞–µ–º –∑–∞–≥–æ–ª–æ–≤–∫–∏ –∏ –æ–ø—Ä–µ–¥–µ–ª—è–µ–º IP-–∞–¥—Ä–µ—Å –æ—Ç–ø—Ä–∞–≤–∏—Ç–µ–ª—è
$headers = function_exists('getallheaders') ? getallheaders() : [];

// –ù–æ—Ä–º–∞–ª–∏–∑—É–µ–º –∏–º–µ–Ω–∞ –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤ –æ–¥–∏–Ω —Ä–∞–∑ –Ω–∞ —Ä–∞–Ω–Ω–µ–º —ç—Ç–∞–ø–µ. –î–∞–ª–µ–µ –≤–µ—Å—å –∫–æ–¥,
// –≤–∫–ª—é—á–∞—è resolveRemoteIp –∏ –ø—Ä–æ–≤–µ—Ä–∫—É —Ç–æ–∫–µ–Ω–∞, —Ä–∞–±–æ—Ç–∞–µ—Ç —Å —É–∂–µ –ø—Ä–∏–≤–µ–¥—ë–Ω–Ω—ã–º–∏ –∫
// –Ω–∏–∂–Ω–µ–º—É —Ä–µ–≥–∏—Å—Ç—Ä—É –∫–ª—é—á–∞–º–∏.
$headers = array_change_key_case($headers, CASE_LOWER);

/**
 * –§–ª–∞–≥ –¥–æ–≤–µ—Ä–∏—è –∫ –∑–∞–≥–æ–ª–æ–≤–∫—É `X-Forwarded-For`.
 * –ï–≥–æ –º–æ–∂–Ω–æ –∑–∞–¥–∞—Ç—å —á–µ—Ä–µ–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –æ–∫—Ä—É–∂–µ–Ω–∏—è `TRUST_FORWARDED`, —É—Å—Ç–∞–Ω–æ–≤–∏–≤
 * –∑–Ω–∞—á–µ–Ω–∏–µ `true` –∏–ª–∏ `1`. –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é –∑–∞–≥–æ–ª–æ–≤–∫—É –Ω–µ –¥–æ–≤–µ—Ä—è–µ–º, —Ç–∞–∫ –∫–∞–∫ –æ–Ω
 * –º–æ–∂–µ—Ç –±—ã—Ç—å –ø–æ–¥–¥–µ–ª–∞–Ω, –µ—Å–ª–∏ –∑–∞–ø—Ä–æ—Å –ø—Ä–æ—Ö–æ–¥–∏—Ç —á–µ—Ä–µ–∑ –Ω–µ–ø—Ä–æ–≤–µ—Ä–µ–Ω–Ω—ã–π –ø—Ä–æ–∫—Å–∏.
 *
 * @var bool $trustForwarded
 */
$trustForwarded = filter_var(
    $_ENV['TRUST_FORWARDED'] ?? getenv('TRUST_FORWARDED'),
    FILTER_VALIDATE_BOOLEAN
);

$remoteIp = resolveRemoteIp($headers, $_SERVER, $trustForwarded); // –ø–µ—Ä–µ–¥–∞—ë–º —É–∂–µ –Ω–æ—Ä–º–∞–ª–∏–∑–æ–≤–∞–Ω–Ω—ã–µ –∑–∞–≥–æ–ª–æ–≤–∫–∏

/**
 * –°–µ–∫—Ä–µ—Ç–Ω—ã–π —Ç–æ–∫–µ–Ω, –∑–∞–¥–∞–Ω–Ω—ã–π –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è.
 * –û—Ç—Å—É—Ç—Å—Ç–≤–∏–µ –∑–Ω–∞—á–µ–Ω–∏—è –¥–æ–ø—É—Å—Ç–∏–º–æ —Ç–æ–ª—å–∫–æ –≤ —Ç–µ—Å—Ç–æ–≤–æ–π —Å—Ä–µ–¥–µ.
 *
 * @var string $secret
 */
$secret = $_ENV['WEBHOOK_SECRET'] ?? getenv('WEBHOOK_SECRET') ?: '';

if ($secret === '') {
    // –í —Ç–µ—Å—Ç–æ–≤–æ–º –æ–∫—Ä—É–∂–µ–Ω–∏–∏ –∑–Ω–∞—á–µ–Ω–∏–µ —Ç–æ–∫–µ–Ω–∞ –º–æ–∂–µ—Ç –æ—Ç—Å—É—Ç—Å—Ç–≤–æ–≤–∞—Ç—å.
    // –í —ç—Ç–æ–º —Å–ª—É—á–∞–µ –ø—Ä–æ–≤–µ—Ä–∫—É —Ç–æ–∫–µ–Ω–∞ –æ—Ç–∫–ª—é—á–∞–µ–º, —á—Ç–æ–±—ã –±—ã–ª–æ —É–¥–æ–±–Ω–æ –æ—Ç–ª–∞–∂–∏–≤–∞—Ç—å
    // –±–æ—Ç–∞ –±–µ–∑ –ø–µ—Ä–µ–¥–∞—á–∏ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã—Ö –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤.
    // ‚ö† –í –±–æ–µ–≤–æ–π —Å—Ä–µ–¥–µ –æ—Ç–∫–ª—é—á–µ–Ω–∏–µ –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–æ–∑–≤–æ–ª–∏—Ç –∑–ª–æ—É–º—ã—à–ª–µ–Ω–Ω–∏–∫–∞–º
    // –æ—Ç–ø—Ä–∞–≤–ª—è—Ç—å –ø–æ–¥–¥–µ–ª—å–Ω—ã–µ –∑–∞–ø—Ä–æ—Å—ã –∫ –Ω–∞—à–µ–º—É webhook –∏ –≤—ã–ø–æ–ª–Ω—è—Ç—å
    // –Ω–µ—Å–∞–Ω–∫—Ü–∏–æ–Ω–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –æ–ø–µ—Ä–∞—Ü–∏–∏.
    $tokenValid = true;
} else {
    // –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º –æ–∂–∏–¥–∞–µ–º—ã–π —Ç–æ–∫–µ–Ω —Å —Ç–µ–º, —á—Ç–æ –ø—Ä–∏—à—ë–ª –≤ –∑–∞–ø—Ä–æ—Å–µ –æ—Ç Telegram.
    $tokenValid = isset($headers['x-telegram-bot-api-secret-token'])
        && hash_equals($secret, $headers['x-telegram-bot-api-secret-token']);
}

// –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –∑–∞–ø—Ä–æ—Å –ø—Ä–∏—à—ë–ª –æ—Ç Telegram (–ø–æ —Ç–æ–∫–µ–Ω—É –∏–ª–∏ IP)
if (!$tokenValid && !isTelegramIP($remoteIp)) {
    // –ó–∞–ø–∏—Å—ã–≤–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –Ω–µ–¥–æ–ø—É—Å—Ç–∏–º–æ–º –∑–∞–ø—Ä–æ—Å–µ, –≤ –ª–æ–≥ –ø–∏—à–µ—Ç—Å—è —Ç–æ–ª—å–∫–æ —á–∞—Å—Ç–∏—á–Ω–æ —Å–∫—Ä—ã—Ç—ã–π —Ç–æ–∫–µ–Ω
    /** @var string $token –¢–æ–∫–µ–Ω –∏–∑ –∑–∞–≥–æ–ª–æ–≤–∫–∞ –∑–∞–ø—Ä–æ—Å–∞ */
    $token = $headers['x-telegram-bot-api-secret-token'] ?? '';
    /** @var string $maskedToken –¢–æ–∫–µ–Ω —Å –ø–æ–∫–∞–∑–∞–Ω–Ω—ã–º–∏ –ø–µ—Ä–≤—ã–º–∏ —á–µ—Ç—ã—Ä—å–º—è —Å–∏–º–≤–æ–ª–∞–º–∏ */
    $maskedToken = $token !== '' ? substr($token, 0, 4) . '***' : '';
    logError('–ù–µ–¥–æ–ø—É—Å—Ç–∏–º—ã–π –∑–∞–ø—Ä–æ—Å: IP ' . $remoteIp . ', —Ç–æ–∫–µ–Ω: ' . $maskedToken);
    http_response_code(403);
    exit;
}

// –°—á–∏—Ç—ã–≤–∞–µ–º —Ç–µ–ª–æ –∑–∞–ø—Ä–æ—Å–∞ –∏ –ø—ã—Ç–∞–µ–º—Å—è –¥–µ–∫–æ–¥–∏—Ä–æ–≤–∞—Ç—å JSON
$body   = file_get_contents('php://input');
$update = json_decode($body, true);
if ($update === null && json_last_error() !== JSON_ERROR_NONE) {
    // –ï—Å–ª–∏ JSON –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–µ–Ω, —Ñ–∏–∫—Å–∏—Ä—É–µ–º —ç—Ç–æ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º 400
    logError('–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π JSON –≤–æ –≤—Ö–æ–¥—è—â–µ–º –∑–∞–ø—Ä–æ—Å–µ: ' . $body);
    http_response_code(400);
    exit;
}
if (!isset($update['message'])) {
    exit;
}
$msg    = $update['message'];
$chatId = $msg['chat']['id'];

try {
    // –ü—ã—Ç–∞–µ–º—Å—è —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å —Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ —Å –±–∞–∑–æ–π –¥–∞–Ω–Ω—ã—Ö
    $pdo = new PDO(DB_DSN, DB_USER, DB_PASSWORD, [
        PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION
    ]);
} catch (PDOException $e) {
    // –ü—Ä–∏ –æ—à–∏–±–∫–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è —É–≤–µ–¥–æ–º–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ –ª–æ–≥–∏—Ä—É–µ–º –¥–µ—Ç–∞–ª–∏
    $userLang = $msg['from']['language_code'] ?? 'ru';
    $text     = $userLang === 'en'
        ? 'Database connection error. Please try again later.'
        : '–û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö. –ü–æ–ø—Ä–æ–±—É–π –ø–æ–∑–∂–µ.';
    logError('–û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –ë–î: ' . $e->getMessage());
    send($text, $chatId);
    exit;
}

// –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –∫–æ–º–∞–Ω–¥—É /start, –¥–æ–ø—É—Å–∫–∞—é—â—É—é –ø–µ—Ä–µ–¥–∞—á—É –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ–≥–æ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞
if (isset($msg['text']) && strncmp($msg['text'], '/start', 6) === 0) {
    /**
     * –ü–∞—Ä–∞–º–µ—Ç—Ä, –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã–π –≤–º–µ—Å—Ç–µ —Å –∫–æ–º–∞–Ω–¥–æ–π /start.
     * –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ —É–∫–∞–∑–∞–ª –∞—Ä–≥—É–º–µ–Ω—Ç, –±—É–¥–µ—Ç –≤–æ–∑–≤—Ä–∞—â–µ–Ω–∞ –ø—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞.
     *
     * @var string $startParam
     */
    $startParam = substr($msg['text'], 7);
    // –ó–¥–µ—Å—å –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏ –º–æ–∂–Ω–æ –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å $startParam (–Ω–∞–ø—Ä–∏–º–µ—Ä, –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä —Ä–µ—Ñ–µ—Ä–∞–ª–∞)

    $kb = [
        'keyboard' => [[[
            'text' => 'üßÆ Calculator',
            'web_app' => ['url' => WEBAPP_URL]
        ]]],
        'resize_keyboard' => true
    ];
